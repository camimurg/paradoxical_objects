class Artist {
    constructor(
        id,
        name,
        surname,
        artworkTitle,
        bildCredit,
        description,
        website,
        video,
        image,
        date,
        curatorChoice
    ) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.artworkTitle = artworkTitle;
        this.bildCredit = bildCredit;
        this.description = description;
        this.website = website;
        this.video = video;
        this.image = image;
        this.date = date;
        this.curatorChoice = curatorChoice;
    }
}

export default Artist;
